function createCharacterGrid(sectionId, dataArray, prefix) {
    const grid = document.getElementById(sectionId);
    grid.innerHTML = '';

    dataArray.forEach(item => {
        const cell = document.createElement('div');
        cell.className = 'character-cell';

        const button = document.createElement('button');
        button.type = 'button';
        button.setAttribute('aria-hidden', 'true');
        button.onclick = function() {
            // Play the sound
            if (typeof item.sound === 'string' && item.sound.trim() !== '') { //Check if a sound is defined for a character to prevent audio errors
               playSound(item);
            }
            //displayCharacter(prefix + " " + item.char, item.image, item.youtubeId);
        };

        const image = document.createElement('img');
        image.src = item.image;
        image.alt = item.char;
        image.width = 100;
        image.height = 71;

        button.appendChild(document.createTextNode(item.char));
        button.appendChild(image);

        cell.appendChild(button);
        grid.appendChild(cell);
    });
}

let currentAudio = null; // To store the currently playing audio object

function playSound(character) {
    try {
        // If there's a currently playing audio, stop it
        if (currentAudio) {
            currentAudio.pause();
            currentAudio.currentTime = 0; // Reset to the beginning of the *entire* clip.  Important!
        }

        const audio = new Audio(`sounds/${character.sound}.mp3`); // Construct the audio file path

        // Set start and end times
        audio.currentTime = character.startTime;
        const endTime = character.endTime;

        audio.addEventListener('timeupdate', function() {
            if (this.currentTime >= endTime) {
                this.pause();
                this.currentTime = character.startTime; // Reset for potential future play
            }
        });

        audio.play();
        currentAudio = audio; // Store the current audio object

    } catch (error) {
        console.error("Error playing sound:", error);
    }
}

function displayCharacter(character, imageSrc, youtubeId) {
    const displayedCharacter = document.getElementById('displayed-character');
    const displayedImage = document.getElementById('displayed-image');
    const displayedImageContainer = document.getElementById('displayed-image-container');
    const videoContainer = document.getElementById('youtube-video-container');
    const closeButton = document.getElementById('close-button'); // Get the close button

    displayedCharacter.textContent = character;
    displayedImage.src = imageSrc;

    if (youtubeId) {
        // Create the iframe for the embedded YouTube video
        const iframe = document.createElement('iframe');
        iframe.width = "640";
        iframe.height = "450";
        iframe.src = `https://www.youtube.com/embed/${youtubeId}`;
        iframe.title = "YouTube video player";
        iframe.frameborder = "0";
        iframe.allow = "accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share";
        iframe.allowFullscreen = true;

        // Initial state: image visible, video hidden
        displayedImage.style.display = 'block';
        videoContainer.style.display = 'none';
        videoContainer.innerHTML = ""; // Clear previous video content if any

        // Set meta tags
        const metaVideo = document.getElementById('meta-youtube-video');
        const metaVideoSecure = document.getElementById('meta-youtube-video-secure');
        const videoUrl = `https://www.youtube.com/embed/${youtubeId}`;
        metaVideo.setAttribute('content', videoUrl);
        metaVideoSecure.setAttribute('content', videoUrl);

        // Click handler to toggle between image and video
        displayedImageContainer.onclick = () => {
            if (displayedImage.style.display === 'block') {
                // Show video, hide image
                displayedImage.style.display = 'none';
                videoContainer.innerHTML = "";  // Ensure the container is empty
                videoContainer.appendChild(iframe); // Add the iframe to videoContainer
                videoContainer.style.display = 'block';
            } else {
                // Show image, hide video
                displayedImage.style.display = 'block';
                videoContainer.style.display = 'none';
                videoContainer.innerHTML = ""; // Clear video container
            }
        };

    } else {
        // Clear the video container and hide it
        videoContainer.innerHTML = '';
        videoContainer.style.display = 'none'; // Hide the video container
        displayedImage.style.display = 'block'; // Ensures image is visible when no video is loaded

        // Clear meta elements
        const metaVideo = document.getElementById('meta-youtube-video');
        const metaVideoSecure = document.getElementById('meta-youtube-video-secure');
        metaVideo.setAttribute('content', '');
        metaVideoSecure.setAttribute('content', '');
        displayedImageContainer.onclick = null; // Remove the click handler, prevents it from firing.
    }

    // Show the close button and set its click handler
    closeButton.style.display = 'block';
    closeButton.onclick = () => {
        displayedImage.style.display = 'none'; // Hide the image
        videoContainer.style.display = 'none'; // Hide the video
        closeButton.style.display = 'none'; // Hide the close button
        displayedCharacter.textContent = ''; // Clear the displayed character
    };
}

